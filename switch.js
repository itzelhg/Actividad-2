let q1 = 10;
switch (q1){
    case 10:
    console.log("Buen trabajo");
    break;

    case 9:
    console.log("Casi perfecto");
    break;

    case 8:
    console.log("Bien, supongo");
    break;
    
    case 7:
    console.log("Esfuerzate");
    break; 

    case 6:
    console.log("Estudia un poco mas");
    break;

    case 5: 
    console.log("Reprobado");
    break;

    default:
    console.log("Error");
    break;
}